<h2>PHP OOP</h2>
<h3>Aufgabe</h3>
<p>
	Schreibe eine Datei <i>oop_01.php</i> und speichere sie in deinem Ordner auf dem localhost.
</p>
<p>
	Diese Datei soll eine Klasse <i>Electronic_device</i> (elektronische Geräte) enthalten. Jedes Gerät 
	besitzt, vom Hersteller angegeben, eine Spannung (engl. <i>voltage</i>). 
<p>
	Die Klasse soll einen entsprechenden Konstruktor enthalten mit möglichen Standardwerten. Außerdem eine 
	Methode mit der die benötigte Spannung geändert werden kann (in Realität durch etwaiges Umrüsten).
</p>
<p>
	Es sollte auch eine Methode existieren, mit der man die Spannung des Geräts ausgeben kann.
<p>
<h3>Zusatz</h3>
<p>
	Den Geräten wird eine weitere Eigenschaft zugewiesen, die den Namen des Geräts enthält und mit ausgegeben wird.
	BEACHTE: Die Eigenschaft muss auch im Konstruktor angegeben werden mit etwaigem Stadard.
<p>
	Weiterhin kann ein Formular geschrieben werden, in das man den entsprechenden Namen des Geräts und seine Spannung 
	eingibt und im Hintergrund das Objekt konstruiert wird und dann sofort ausgegeben wird auf dem Bildschirm.
</p>