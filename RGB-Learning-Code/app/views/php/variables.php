<h2>Variablen</h2>
<h3>Erstellung</h3>
<p>
	Variablen werden mit einem Dollarzeichen <i>$</i>, gefolgt von einem Bezeichner (engl.) erstellt.<br>
	Es gibt verschiedene Begriffe zwischen denen man unterscheidet: Deklaration, Initialisierung, Definition.
</p>
<h3>Begriffe</h3>
<p>
	Als Deklaration bezeichnet man das erstellen einer Variablen, also die Bereitstellung von Arbeitsspeicher,
	aber ohne das Zuweisen eines Wertes.
</p>
