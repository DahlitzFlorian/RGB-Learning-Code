<h2>Erläuterung von objektorientiertem PHP</h2>
<p>
	Der Sinn hinter objektorientiertem Programmieren ist es, Objekte und deren Interaktion aus der <i>realen Welt</i>
	originalgetreu darzustellen.
</p>
<p>
	Man versucht die Wartung und die Erweiterung des Programmes damit zu vereinfachen und es für uns Menschen noch
	verständlicher zu gestalten.
</p>