<h2>Eigenschaften</h2>
<h3>Allgemein</h3>
<p>
	Jeder Klasse können bestimmte Eigenschaften zugewiesen werden, die jedes Objekt dieser Klasse, welches später
	erstellt werden soll, besitzt.
</p>
<h3>Eigenschaften zuweisen</h3>
<p>
	Das zuweisen von Eigenshaften ist ziemlich simpel: Man deklariert in der Klasse einfach Variablen mit den Eigenschaften als
	Bezeichner.
</p>
<p>
	Vor dem Bezeichner und dem Dollerzeichen kommt noch das Schlüsselwort <i>private</i>, sodass man von außerhalb der Klasse
	nicht auf diese zugreifen kann.
</p>
